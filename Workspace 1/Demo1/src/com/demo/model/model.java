package com.demo.model;

public class model {
		private int id;
		private String fname;
		public model() {
			
		}
		
		public model(String fname) {
			super();
			this.fname = fname;
		}

		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getFname() {
			return fname;
		}
		public void setFname(String fname) {
			this.fname = fname;
		}
				

	}
